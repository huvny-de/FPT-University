/**
 *
 * @author Henry
 */
public class Book {

    String code, title, publisher, type;

    public Book(String code, String title, String publisher, String type) {
        this.code = code;
        this.title = title;
        this.publisher = publisher;
        this.type = type;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "Book: (" + code + ", " + title + ", " + publisher + ", " + type + ')';
    }

}
