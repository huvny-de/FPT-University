    private void btnSaveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveItemActionPerformed
        // TODO add your handling code here:
        if (!modelItem.validDate(txtCodeItem.getText().trim(), txtNameItem.getText().trim(), txtUnit.getText().trim(), txtPrice.getText().trim(), CBSupplier.getSelectedItem())) {
            return;
        }
        if (addNewItem) {
            try {
                if (!modelItem.validDateCodeItem(txtCodeItem.getText().trim())) {
                    JOptionPane.showMessageDialog(this, "DUPLICATED ITEMCODE at " + txtCodeItem.getText().trim());
                    return;
                }
                SE140364_Item it = new SE140364_Item(txtCodeItem.getText().toUpperCase().trim(), txtNameItem.getText().trim(), txtUnit.getText().trim(),
                        Float.parseFloat(txtPrice.getText().trim()), CKsupplying.isSelected(), modelItem.loadSupCodeIntoItems(CBSupplier.getSelectedItem().toString()));
                if (modelItem.Insert(it) == SUCCESS) {
                    modelItem.getModeItem().getList().add(it);
                }
                addNewItem = false;
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            try {
                int selectedRow = tblItem.getSelectedRow();
                if (selectedRow < 0) {
                    JOptionPane.showMessageDialog(this, "Please Choose Row to Edit");
                }

                SE140364_Item itOld = modelItem.getModeItem().getList().get(selectedRow);
                SE140364_Item itNew = new SE140364_Item(txtCodeItem.getText().trim().toUpperCase(), txtNameItem.getText().trim(), txtUnit.getText().trim(), Float.parseFloat(txtPrice.getText().trim()), CKsupplying.isSelected(), modelItem.loadSupCodeIntoItems(CBSupplier.getSelectedItem().toString()));
                if (modelItem.Save(itNew) == SUCCESS) {
                    itOld.setItemName(itNew.getItemName());
                    itOld.setPrice(itNew.getPrice());
                    itOld.setUnit(itNew.getUnit());
                    itOld.setSupCode(itNew.getSupCode());
                    itOld.setSupplying(itNew.isSupplying());

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        tblItem.updateUI();
    }//GEN-LAST:event_btnSaveItemActionPerformed

