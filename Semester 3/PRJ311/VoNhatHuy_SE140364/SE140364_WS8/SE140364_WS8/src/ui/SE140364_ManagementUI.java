    private void btnSaveSupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveSupActionPerformed
        // TODO add your handling code here:
        if (!modelSuppliers.validDate(txtSupCode.getText().trim(), txtSupName.getText().trim(), txtAddressSup.getText().trim())) {
            return;
        }
        if (addNewSup) {
            try {
                if (!modelSuppliers.validDateCodeSupplier(txtSupCode.getText().trim())) {
                    JOptionPane.showMessageDialog(this, "DUPLICATE SUPCODE AT " + txtSupCode.getText().trim());
                    return;
                }
                SE140364_Suppliers sup = new SE140364_Suppliers(txtCodeItem.getText().trim(), txtSupName.getText().trim(), txtAddressSup.getText().trim(), CKcoll.isSelected());
                if (modelSuppliers.Insert(sup) == SUCCESS) {
                    modelSuppliers.getModelSup().getList().add(sup);
                    modelSuppliers.getListSuppliersComboBox().add(sup.getSupCode() + " - " + sup.getSupCode());
                }

                addNewSup = false;
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            try {
                int selectedRow = tblSuppliers.getSelectedRow();
                if (selectedRow < 0) {
                    JOptionPane.showMessageDialog(this, "Choose Row to Exit");
                    return;
                }

                SE140364_Suppliers supNew = new SE140364_Suppliers(txtSupCode.getText().trim(), txtSupName.getText().trim(), txtAddressSup.getText().trim(), CKcoll.isSelected());
                SE140364_Suppliers supOld = modelSuppliers.getModelSup().getList().get(selectedRow);

                if (modelSuppliers.Save(supNew) == SUCCESS) {
                    modelSuppliers.getListSuppliersComboBox().remove(supOld.getSupCode() + " - " + supOld.getSupName());
                    supOld.setSupName(supNew.getSupName());
                    supOld.setAddress(supNew.getAddress());
                    supOld.setColloborating(supNew.isColloborating());
                    modelSuppliers.getListSuppliersComboBox().add(supNew.getSupCode() + " - " + supNew.getSupName());

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        modelItem.getModeItem().loadSuppliersComboBox();
        CBSupplier.setModel(new DefaultComboBoxModel<>(modelSuppliers.getListSuppliersComboBox()));
        tblSuppliers.updateUI();
    }//GEN-LAST:event_btnSaveSupActionPerformed

    private void btnDeleteSupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSupActionPerformed
        // TODO add your handling code here:
        try {
            int selectedRow = tblSuppliers.getSelectedRow();
            if (selectedRow < 0 || deleteSup == false) {
                JOptionPane.showMessageDialog(this, "Choose the Row to Delete");
                return;
            }
            int r = JOptionPane.showConfirmDialog(this, "Do you want to Delete this Supplier?");
            if (r == JOptionPane.YES_OPTION) {
                SE140364_Suppliers sup = modelSuppliers.getModelSup().getList().get(selectedRow);
                if (!modelItem.checkConstraintSupCode(sup.getSupCode())) {
                    JOptionPane.showMessageDialog(this, "Still Items is manager by this Supplier");
                    return;
                }
                if (modelSuppliers.Delete(sup) == SUCCESS) {
                    deleteSup = false;
                    modelSuppliers.getModelSup().getList().remove(sup);
                    modelSuppliers.getListSuppliersComboBox().remove(sup.getSupCode() + " - " + sup.getSupName());
                    JOptionPane.showMessageDialog(this, "Deleted");
                    CBSupplier.setModel(new DefaultComboBoxModel<>(modelSuppliers.getListSuppliersComboBox()));
                    clearInformationSupplierOnTable();
                    modelItem.getModeItem().loadSuppliersComboBox();
                    tblSuppliers.updateUI();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteSupActionPerformed

