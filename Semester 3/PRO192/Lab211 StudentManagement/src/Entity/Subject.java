package Entity;

/**
 *
 * @author Henry
 */
public class Subject {

    String id, sbName;
    int credit;

    public Subject(String id, String sbName, int credit) {
        this.id = id;
        this.sbName = sbName;
        this.credit = credit;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSbName() {
        return sbName;
    }

    public void setSbName(String sbName) {
        this.sbName = sbName;
    }

    public int getCredit() {
        return credit;
    }

    public void setCredit(int credit) {
        this.credit = credit;
    }

    @Override
    public String toString() {
        return "Subject{" + "id=" + id + ", sbName=" + sbName + ", credit=" + credit + '}';
    }

}
